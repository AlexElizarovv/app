	public static function getNames( $langCode ) {
		$actualLangCode = $langCode;

		if ($langCode == 'pt-br') {
			$actualLangCode == 'pt';
		} else if ($langCode == 'zh-cn') {
			$actualLangCode == 'zh-hans';
		} else if ($langCode == 'zh-tw') {
			$actualLangCode == 'zh-hant';
		}

		if (array_key_exists($actualLangCode, self::$countryNames) !== true) {
			$actualLangCode = 'en';
		}

		return self::$countryNames[$actualLangCode];
	}
